// @ts-check

const SHAPE_ROCK = 'A';
const SHAPE_PAPER = 'B';
const SHAPE_SCISSORS = 'C';

const RIGHT_LOSE = 'X';
const RIGHT_DRAW = 'Y';
const RIGHT_WIN = 'Z';

const SCORE_WIN = 6;
const SCORE_LOSE = 0;
const SCORE_DRAW = 3;

const SCORE_ROCK = 1;
const SCORE_PAPER = 2;
const SCORE_SCISSORS = 3;

let totalScore = 0;
getInput()
  .split('\n')
  .forEach((row) => {
    const play = row.split(' ');

    const left = play[0];
    const right = play[1];

    let rightShape = '';
    if (right === RIGHT_DRAW) {
      rightShape = left;
    } else {
      switch (left) {
        case SHAPE_ROCK:
          if (right === RIGHT_WIN) {
            rightShape = SHAPE_PAPER;
          } else if (right === RIGHT_LOSE) {
            rightShape = SHAPE_SCISSORS;
          }
          break;

        case SHAPE_PAPER:
          if (right === RIGHT_WIN) {
            rightShape = SHAPE_SCISSORS;
          } else if (right === RIGHT_LOSE) {
            rightShape = SHAPE_ROCK;
          }
          break;

        case SHAPE_SCISSORS:
          if (right === RIGHT_WIN) {
            rightShape = SHAPE_ROCK;
          } else if (right === RIGHT_LOSE) {
            rightShape = SHAPE_PAPER;
          }
          break;

        default:
          break;
      }
    }

    totalScore += getOutcomeScore(right) + getShapeScore(rightShape);
  });

console.log(totalScore);

function getOutcomeScore(outcome) {
  switch (outcome) {
    case RIGHT_LOSE:
      return SCORE_LOSE;

    case RIGHT_DRAW:
      return SCORE_DRAW;

    case RIGHT_WIN:
      return SCORE_WIN;
  }

  throw new Error();
}

function getShapeScore(shape) {
  switch (shape) {
    case SHAPE_PAPER:
      return SCORE_PAPER;

    case SHAPE_ROCK:
      return SCORE_ROCK;

    case SHAPE_SCISSORS:
      return SCORE_SCISSORS;
  }

  throw new Error();
}

