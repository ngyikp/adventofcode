function getCharCode(letter) {
  const CHAR_CODE_A_UPPERCASE = 65;
  const CHAR_CODE_Z_UPPERCASE = 90;

  const CHAR_CODE_A_LOWERCASE = 97;
  const CHAR_CODE_Z_LOWERCASE = 122;

  const charCode = letter.charCodeAt(0);
  if (charCode >= CHAR_CODE_A_LOWERCASE && charCode <= CHAR_CODE_Z_LOWERCASE) {
    return charCode - CHAR_CODE_A_LOWERCASE + 1;
  }

  if (charCode >= CHAR_CODE_A_UPPERCASE && charCode <= CHAR_CODE_Z_UPPERCASE) {
    return charCode - CHAR_CODE_A_UPPERCASE + 26 + 1;
  }

  throw new Error();
}

