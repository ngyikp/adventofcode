const LEFT_ROCK = 'A';
const LEFT_PAPER = 'B';
const LEFT_SCISSORS = 'C';

const RIGHT_ROCK = 'X';
const RIGHT_PAPER = 'Y';
const RIGHT_SCISSORS = 'Z';

const SCORE_WIN = 6;
const SCORE_LOSE = 0;
const SCORE_DRAW = 3;

const SCORE_ROCK = 1;
const SCORE_PAPER = 2;
const SCORE_SCISSORS = 3;

let totalScore = 0;
getInput()
  .split('\n')
  .forEach((row) => {
    const play = row.split(' ');

    const left = play[0];
    const right = play[1];

    let score = getShapeScore(right);
    switch (left) {
      case LEFT_ROCK:
        if (right === RIGHT_ROCK) {
          score += SCORE_DRAW;
        } else if (right === RIGHT_PAPER) {
          score += SCORE_WIN;
        } else if (right === RIGHT_SCISSORS) {
          score += SCORE_LOSE;
        }
        break;

      case LEFT_PAPER:
        if (right === RIGHT_ROCK) {
          score += SCORE_LOSE;
        } else if (right === RIGHT_PAPER) {
          score += SCORE_DRAW;
        } else if (right === RIGHT_SCISSORS) {
          score += SCORE_WIN;
        }
        break;

      case LEFT_SCISSORS:
        if (right === RIGHT_ROCK) {
          score += SCORE_WIN;
        } else if (right === RIGHT_PAPER) {
          score += SCORE_LOSE;
        } else if (right === RIGHT_SCISSORS) {
          score += SCORE_DRAW;
        }
        break;

      default:
        break;
    }

    totalScore += score;
  });

console.log(totalScore);

function getShapeScore(shape) {
  switch (shape) {
    case RIGHT_PAPER:
      return SCORE_PAPER;

    case RIGHT_ROCK:
      return SCORE_ROCK;

    case RIGHT_SCISSORS:
      return SCORE_SCISSORS;
  }
}

